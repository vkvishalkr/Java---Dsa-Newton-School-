package loop; 

public class TwoLoop {
    
    public static void main(String[] args) {
        int i;           // i = 10
        int j;           // j = 10

        for(i=0; i<5; i++) {            //  ;
            for(j=0; j<5; j++) {        //  ;
                System.out.print(j);
            }
            System.out.println();
        }
    } 

}
